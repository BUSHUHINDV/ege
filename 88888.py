'''
(А.Н. Носкин) Петя составляет список из 5-буквенных слов, в состав которых входят только буквы А, О, У.
 Петя расположил слова в обратном алфавитном порядке. Вот начало списка:
1. УУУУУ
2. УУУУО
3. УУУУА
4. УУУОУ
...
Запишите слово, которое стоит в этом списке под номером 100.
'''
i=1
l=['У','О','А']
for a1 in l:
    for a2 in l:
        for a3 in l:
            for a4 in l:
                for a5 in l:
                    print(i,'. ', a1,a2,a3,a4,a5,sep='')
                    i+=1